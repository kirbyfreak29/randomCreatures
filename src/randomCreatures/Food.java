package randomCreatures;

/**
 * Represents an instance of food.
 * 
 * @author kirbyfreak29
 */
public class Food {
	
	// Variables
	int foodValue;
	
	
	/**
	 * Constructor
	 * @param foodValue	int, the value of the food
	 */
	public Food(int foodValue) {
		this.foodValue = foodValue;
	}
	
	// Getters and Setters
	public int getFoodValue() { return foodValue; }
	
}
